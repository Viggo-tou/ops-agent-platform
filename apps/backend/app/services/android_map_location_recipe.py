def _wire_payload_fields(content: str) -> str:
    names = _field_names(content)
    replacements = {
        "houseNumber": "houseNumber",
        "street": "street",
        "area": "area",
        "division": "division",
        "district": "district",
        "thana": "thana",
        "city": "city",
        "country": "country",
        "postcode": names["post_code"],
        "postCode": names["post_code"],
        "notes": names["notes"],
    }
    new_content = content
    for key, var_name in replacements.items():
        new_content = re.sub(
            rf'("{re.escape(key)}"\s+to\s+)""',
            rf"\1{var_name}",
            new_content,
        )
    new_content = re.sub(r'("latitude"\s+to\s+)0(?:\.0)?\b', r"\1latitude", new_content)
    new_content = re.sub(r'("longitude"\s+to\s+)0(?:\.0)?\b', r"\1longitude", new_content)
    new_content = _ensure_payload_entry(new_content, "latitude", "latitude")
    new_content = _ensure_payload_entry(new_content, "longitude", "longitude")
    return new_content


def _ensure_payload_entry(content: str, key: str, var_name: str) -> str:
    if re.search(rf'"{re.escape(key)}"\s+to\s+', content):
        return content
    lines = content.splitlines()
    map_ranges = _mapof_ranges(lines)
    for start, end in map_ranges:
        for idx in range(start, end):
            if re.search(r'"notes"\s+to\s+|\bnotes\s*=', lines[idx]):
                indent = re.match(r"^(\s*)", lines[idx]).group(1)  # type: ignore[union-attr]
                if not lines[idx].rstrip().endswith(","):
                    lines[idx] = lines[idx].rstrip() + ","
                lines.insert(idx + 1, f'{indent}"{key}" to {var_name},')
                return _join_like(content, lines)
    return content


def _mapof_ranges(lines: list[str]) -> list[tuple[int, int]]:
    ranges: list[tuple[int, int]] = []
    for idx, line in enumerate(lines):
        if "mapOf(" not in line:
            continue
        balance = 0
        seen = False
        for end in range(idx, len(lines)):
            for ch in lines[end]:
                if ch == "(":
                    balance += 1
                    seen = True
                elif ch == ")":
                    balance -= 1
            if seen and balance <= 0:
                ranges.append((idx, end + 1))
                break
    return ranges


def _coverage_payload(
    *,
    file_path: str,
    had_map_ui: bool,
    had_geocoder_locale: bool,
    had_persisted_coords: bool,
) -> dict[str, Any]:
    implemented: list[dict[str, str]] = []
    no_change: list[dict[str, str]] = []

    def add(cid: str, *, already: bool, quote: str) -> None:
        row = {
            "contract_id": cid,
            "file_path": file_path,
            "evidence_quote": quote,
            "evidence_mode": "preexisting" if already else "recipe_diff",
        }
        if already:
            no_change.append(row)
        else:
            implemented.append(row)

    add("map_ui_present", already=had_map_ui, quote="MapEventsOverlay")
    add("user_can_select_location", already=had_map_ui, quote="singleTapConfirmedHelper")
    add("location_updates_form_state", already=had_map_ui, quote="getFromLocation")
    add("persisted_to_storage", already=had_persisted_coords, quote='"latitude" to latitude')
    add("installed_library_only", already=had_map_ui, quote="org.osmdroid.")
    add(
        "geocoder_lifecycle_safe",
        already=had_geocoder_locale,
        quote="Geocoder(ctx, Locale.getDefault())",
    )
    return {
        "implemented_contracts": implemented,
        "verified_no_change_contracts": no_change,
        "unimplemented_contracts": [],
    }


def _join_like(original: str, lines: list[str]) -> str:
    text = "\n".join(lines)
    return text + ("\n" if original.endswith("\n") else "")


def _unified_diff(path: str, before: str, after: str) -> str:
    before_lines = before.splitlines(keepends=True)
    after_lines = after.splitlines(keepends=True)
    body = "".join(
        difflib.unified_diff(
            before_lines,
            after_lines,
            fromfile=f"a/{path}",
            tofile=f"b/{path}",
            n=3,
        )
    )
    return f"diff --git a/{path} b/{path}\n{body}" if body else ""
